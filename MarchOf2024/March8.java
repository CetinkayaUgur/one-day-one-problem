package MarchOf2024;

public class March8 {
    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        ListNode intersectVal = new ListNode(0);
        if(headA == null || headB == null) return intersectVal;
        
        return intersectVal;
    }
}

class ListNode {
    int val;
    ListNode next;
    ListNode(int x) {
        val = x;
        next = null;
    }
}